package com.joe.mvc.config;

public class Config {
	
	private String account;
	private String newReply;
	private String newMsg;
	private String newLocationReply;
	private String result;
	private String noResult;
	
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getNewReply() {
		return newReply;
	}
	public void setNewReply(String newReply) {
		this.newReply = newReply;
	}
	public String getNewMsg() {
		return newMsg;
	}
	public void setNewMsg(String newMsg) {
		this.newMsg = newMsg;
	}
	public String getNewLocationReply() {
		return newLocationReply;
	}
	public void setNewLocationReply(String newLocationReply) {
		this.newLocationReply = newLocationReply;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getNoResult() {
		return noResult;
	}
	public void setNoResult(String noResult) {
		this.noResult = noResult;
	}

}
